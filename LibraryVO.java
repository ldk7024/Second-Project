package kr.smhrd.model;

public class LibraryVO {
	private int library_idx;
	private String library_name;
	private String library_loc;
	
	public int getLibrary_idx() {
		return library_idx;
	}
	public void setLibrary_idx(int library_idx) {
		this.library_idx = library_idx;
	}
	public String getLibrary_name() {
		return library_name;
	}
	public void setLibrary_name(String library_name) {
		this.library_name = library_name;
	}
	public String getLibrary_loc() {
		return library_loc;
	}
	public void setLibrary_loc(String library_loc) {
		this.library_loc = library_loc;
	}
	
	
}
